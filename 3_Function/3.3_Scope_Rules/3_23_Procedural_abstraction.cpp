/*
What is the principle of procedural abstraction as applied to function
definitions?
*/
// The principle of procedural abstraction is that the details of how a function
// accomplishes its task are hidden from the user of the function. The user only
// needs to know the function's name, its parameters, and what it returns (if
// anything). This allows the user to call the function without needing to
// understand its internal workings, promoting code reuse and modularity.
// Formally speaking:
// The principle of procedural abstraction says that a function should be written so
// that it can be used like a black box. This means that the programmer who uses
// the function needs not look at the body of the function definition to see how the
// function works. The function declaration and accompanying comment should be
// all the programmer needs in order to use the function.