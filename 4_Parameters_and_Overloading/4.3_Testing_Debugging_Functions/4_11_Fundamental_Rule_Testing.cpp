// What is the fundamental rule for testing functions? Why is this a good way to
// test functions?

// Answer: The fundamental rule for testing functions is to test each function
// independently before integrating it into a larger program. This approach is
// often referred to as unit testing.

// Formally:
// The fundamental rule for testing functions is that every function should be tested
// in a program in which every other function in that program has already been fully
// tested and debugged. This is a good way to test a function because if you follow this
// rule, then when you find a bug, you will know which function contains the bug.