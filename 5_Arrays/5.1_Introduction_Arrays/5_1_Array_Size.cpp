/*
Describe the difference in the meaning of int a[5]; and the meaning of
a[4]. What is the meaning of the [5] and [4] in each case?
*/

// Answer:
// In the declaration int a[5];, the [5] indicates that 'a' is an array that can hold 5 elements of type int.
// In the expression a[4], the [4] is used to access the fifth element of the array 'a', since array indexing starts at 0.
// Technically, a[4] is the last element of the array 'a'.